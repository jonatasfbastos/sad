     private boolean validarCampos() {
         StringUtil validacao = StringUtil.getInstance();
        
         if (validacao.isEmpty(this.perfilUsuario.getNome()) || 
                 validacao.isEmpty(this.perfilUsuario.getDescricao()) ) {
            return false;
         }
                 
        return true;
     }
     
