     private boolean validarCampos() {
         StringUtil validacao = StringUtil.getInstance();
         if (validacao.isEmpty(this.perfilEdicao.getNome()) || 
                 validacao.isEmpty(this.perfilEdicao.getDescricao()) ) {
            return false;
         }
        return true;
     }
     
