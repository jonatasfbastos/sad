    private boolean validarCampos() {
        StringUtil validacao = StringUtil.getInstance();
        if (validacao.isEmpty(this.modalidadeEdicao.getNome()) || validacao.isEmpty(this.modalidadeEdicao.getDescricao()) ) {
            return false;
        }
        return true;
    }
    
