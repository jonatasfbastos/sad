    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String descricao = txtDescricao.getText();
        
        this.modalidadeEdicao.setNome(nome);
        this.modalidadeEdicao.setDescricao(descricao);
        
        if (this.validarCampos() == false) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos e tente novamente!", 
                    "Preencha os Campos!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        try {
            if(JOptionPane.showConfirmDialog(null, "Deseja realmente editar " +
                       this.modalidadeEdicao.getNome() + "?", "Editar Dados", JOptionPane.DEFAULT_OPTION) == 0) {
                this.facade.updateModalidade(this.modalidadeEdicao);
                this.telaExibir.atualizaTabela();
            }
            this.setVisible(false);
            this.telaExibir.setVisible(true);
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, error, 
                    "Erro ao editar!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

