     // Define a etapa que será editada e mostra os dados nos campos da tela.
     public void setEtapa(EtapaCurso etapaCurso) {
          this.etapa = etapaCurso;
          
          txtNome.setText(etapaCurso.getNome());
          txtCargaHoraria.setText(Integer.toString(etapaCurso.getCargaHoraria()));
          txtPeriodo.setText(Integer.toString(etapaCurso.getPeriodo()));
          tlgConcluinte.getModel().setSelected(etapaCurso.isConcluinte());
     }
     
     private boolean validarCampos(EtapaCurso etapa) {
         StringUtil validacao = StringUtil.getInstance();
         if (validacao.isEmpty(etapa.getNome()) || 
                 validacao.isEmpty(Integer.toString(etapa.getCargaHoraria())) || 
                 validacao.isEmpty(Integer.toString(etapa.getPeriodo()))) {
            return false;
         }
        return true;
     }
     
