    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // Obtém os novos dados.
        String nome = txtNome.getText();
        boolean concluinte = tlgConcluinte.getModel().isSelected();
        int cargaHoraria, periodo;
        
        try {
            cargaHoraria = Integer.parseInt(txtCargaHoraria.getText());
            periodo = Integer.parseInt(txtPeriodo.getText());
        } catch (NumberFormatException error) {
            JOptionPane.showMessageDialog(null, error, 
                    "Carga horária e período aceitam apenas números inteiros!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Define os novos dados.
        this.etapa.setNome(nome);
        this.etapa.setCargaHoraria(cargaHoraria);
        this.etapa.setPeriodo(periodo);
        this.etapa.setConcluinte(concluinte);
        
        if (this.validarCampos(etapa) == false) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos e tente novamente!", 
                    "Preencha os Campos!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        /* 
            Tenta atualizar a etapa e depois mostra a tela de visualização com os novos dados.
            Caso ocorra um erro, mostra na tela.
        */
        try {
            if(JOptionPane.showConfirmDialog(null, "Deseja realmente editar " +
                       this.etapa.getNome() + "?", "Editar Dados", JOptionPane.DEFAULT_OPTION) == 0) {
                this.facade.updateEtapaCurso(this.etapa);
                this.telaExibir.atualizaTabela();
            }
            this.setVisible(false);
            this.telaExibir.setVisible(true);
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, error, 
                    "Erro ao editar!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

